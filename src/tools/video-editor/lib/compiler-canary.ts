/** Options for the compiler canary. */
export interface CompilerCanaryOptions {
  /** The extension ID that owns generated objects. */
  extensionId: string;
  /** Live TimelineOps adapter (ctx.creative.timeline). */
  timeline: TimelineOps;
  /** Live TimelineReader adapter (ctx.creative.reader). */
  reader: TimelineReader;
  /** Live ProposalRuntime adapter (ctx.creative.proposals). */
  proposals: ProposalRuntime;
}

/**
 * The compiler canary public API.
 *
 * Extensions call `processDsl()` to parse a DSL, store source/source-map data,
 * and emit a TimelineProposal.  They call `cleanupOwnObjects()` to remove all
 * objects generated by this extension from the timeline.
 */
export interface CompilerCanary {
  /**
   * Process a DSL string: parse, snapshot, compute diff, store source +
   * source-map data, and create a TimelineProposal.
   *
   * Returns the result including the proposal and generated clip IDs.
   * Returns null if the DSL is invalid.
   */
  processDsl(dslJson: string): CompilerCanaryResult | null;

  /**
   * Create a proposal that removes all clips owned by this extension
   * from the current timeline.
   */
  cleanupOwnObjects(rationale?: string): CompilerCanaryResult;

  /** The extension ID. */
  readonly extensionId: string;
}

/**
 * Create a compiler canary instance for the given extension.
 *
 * The canary uses only the public creative context services — it never
 * imports or accesses internal timeline stores, DataProvider, or
 * mutation engines.
 */
export function createCompilerCanary(
  options: CompilerCanaryOptions,
): CompilerCanary {
  const { extensionId, timeline, reader, proposals } = options;

  function processDsl(dslJson: string): CompilerCanaryResult | null {
    // 1. Parse DSL
    const dsl = parseDsl(dslJson);
    if (!dsl) return null;

    // 2. Read current timeline snapshot
    const snapshot = reader.snapshot();

    // 3. Build DSL and source-map project-data ops
    const dslOps = buildDslWriteOps(extensionId, dsl);
    const clipIdToSmId = new Map<string, string>();
    const smOps = buildSourceMapOps(extensionId, dsl, clipIdToSmId);

    // 4. Determine which clips need to be added, updated, or removed
    const dslClipIds = new Set(dsl.clips.map((c) => c.id));
    const removeOps = buildClipRemoveOps(extensionId, snapshot, dslClipIds);
    const updateOps = buildClipUpdateOps(extensionId, snapshot, dsl, clipIdToSmId);
    const addOps = buildClipAddOps(extensionId, snapshot, dsl, clipIdToSmId);

    // 5. Write source + source-map data via TimelineOps.apply()
    const dataPatch: TimelinePatch = {
      version: snapshot.baseVersion,
      operations: [...dslOps, ...smOps],
      source: extensionId,
      meta: { kind: 'compiler-canary-data' },
    };

    if (dataPatch.operations.length > 0) {
      timeline.apply(dataPatch);
    }

    // 6. Build the main patch (clip mutations)
    const mainOperations = [...removeOps, ...updateOps, ...addOps];
    const generatedClipIds = [
      ...addOps.map((o) => o.target),
      ...updateOps.map((o) => o.target),
    ];
    const sourceMapEntryIds = [...clipIdToSmId.values()];

    if (mainOperations.length === 0) {
      // No clip changes needed — return result without creating a proposal
      return {
        generatedClipIds,
        sourceMapEntryIds,
      };
    }

    const mainPatch: TimelinePatch = {
      version: snapshot.baseVersion + 1, // account for data patch above
      operations: mainOperations,
      source: extensionId,
      meta: { kind: 'compiler-canary' },
    };

    // 7. Create proposal
    const input: TimelineProposalInput = {
      source: extensionId,
      rationale: `Compiler canary: ${addOps.length} added, ${updateOps.length} updated, ${removeOps.length} removed`,
      patch: mainPatch,
      baseVersion: reader.snapshot().baseVersion,
    };

    const proposal = proposals.create(input);

    return {
      proposal,
      generatedClipIds,
      sourceMapEntryIds,
    };
  }

  function cleanupOwnObjects(rationale?: string): CompilerCanaryResult {
    const snapshot = reader.snapshot();
    const ownedClipIds = snapshot.clips
      .filter((c) => isOwnedByExtension(c, extensionId))
      .map((c) => c.id);

    if (ownedClipIds.length === 0) {
      return { generatedClipIds: [], sourceMapEntryIds: [] };
    }

    const removeOps: TimelinePatchOperation[] = ownedClipIds.map((id) => ({
      op: 'clip.remove' as const,
      target: id,
    }));

    const patch: TimelinePatch = {
      version: snapshot.baseVersion,
      operations: removeOps,
      source: extensionId,
      meta: { kind: 'compiler-canary-cleanup' },
    };

    const input: TimelineProposalInput = {
      source: extensionId,
      rationale: rationale ?? `Compiler canary cleanup: removing ${ownedClipIds.length} owned clip(s)`,
      patch,
      baseVersion: snapshot.baseVersion,
    };

    const proposal = proposals.create(input);

    return {
      proposal,
      generatedClipIds: [],
      sourceMapEntryIds: [],
    };
  }

  return {
    processDsl,
    cleanupOwnObjects,
    extensionId,
  };
}

/**
 * Create a ReighExtension that wraps the compiler canary.
 *
 * This is the standard defineExtension() shape consumed by the host
 * extension lifecycle.  The activate() function receives the live
 * ExtensionContext and wires up the compiler canary using the creative
 * context services (timeline, reader, proposals).
 *
 * The extension stores its last DSL in settings and can be triggered
 * to re-process by updating the setting.
 */
export function createCompilerCanaryExtension(
  extensionId: string,
  label?: string,
): { manifest: { id: string; version: string; label: string; description: string; apiVersion: number }; activate: (ctx: ExtensionContext) => DisposeHandle } {
  return {
    manifest: {
      id: extensionId,
      version: '1.0.0',
      label: label ?? 'Compiler Canary',
      description: 'M3 compiler canary extension that exercises the full public SDK contract.',
      apiVersion: 1,
    },
    activate(ctx: ExtensionContext): DisposeHandle {
      const canary = createCompilerCanary({
        extensionId,
        timeline: ctx.creative.timeline,
        reader: ctx.creative.reader,
        proposals: ctx.creative.proposals,
      });

      // Read DSL from settings
      const dslJson = ctx.services.settings.get<string>('compiler-canary.dsl');

      let lastResult: CompilerCanaryResult | null = null;

      if (dslJson) {
        lastResult = canary.processDsl(dslJson);
        if (lastResult) {
          ctx.services.diagnostics.report({
            severity: 'info',
            code: 'compiler-canary/processed',
            message: `Compiler canary processed DSL: ${lastResult.generatedClipIds.length} clips generated, proposal ${lastResult.proposal?.id ?? 'none'}`,
          });
        } else {
          ctx.services.diagnostics.report({
            severity: 'warning',
            code: 'compiler-canary/parse-failed',
            message: 'Compiler canary failed to parse DSL from settings.',
          });
        }
      } else {
        ctx.services.diagnostics.report({
          severity: 'info',
          code: 'compiler-canary/no-dsl',
          message: 'Compiler canary activated with no DSL in settings.',
        });
      }

      return {
        dispose(): void {
          // Cleanup only our own generated objects
          canary.cleanupOwnObjects('Extension deactivated — cleaning up owned objects');
          ctx.services.diagnostics.report({
            severity: 'info',
            code: 'compiler-canary/disposed',
            message: 'Compiler canary disposed. Owned objects cleaned up.',
          });
        },
      };
    },
  };
}
