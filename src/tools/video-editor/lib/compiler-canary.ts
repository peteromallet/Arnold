/**
 * Compiler Canary — extension-owned DSL compiler that exercises the full
 * M3 public extension contract.
 *
 * The compiler canary:
 * 1. Reads the current timeline via ctx.creative.reader.snapshot()
 * 2. Stores DSL source + source-map data in extension project-data namespace
 *    via ctx.creative.timeline.apply() with project-data.write ops
 * 3. Creates TimelineProposal objects via ctx.creative.proposals.create()
 *    to add, update, or delete generated clips
 * 4. Stamps GeneratedObjectMeta (extensionId, contributionId, provenance,
 *    generatedAt, sourceMapEntryId) on every generated clip/track
 * 5. Proposes add/update/delete operations only for objects it owns
 *    (identified by __generated__ metadata)
 * 6. On cleanup, removes only its own generated objects from the timeline
 *
 * The canary uses ONLY public SDK types from @reigh/editor-sdk. It does not
 * import from video-editor internals (DataProvider, TimelineData, store, etc.).
 *
 * @publicContract
 */

import type {
  TimelineOps,
  TimelineReader,
  TimelineSnapshot,
  TimelinePatch,
  TimelinePatchOperation,
  TimelineProposalInput,
  TimelineProposal,
  ProposalRuntime,
  TimelineClipSummary,
  TimelineTrackSummary,
  GeneratedObjectMeta,
  SourceMapEntry,
  DisposeHandle,
  ExtensionContext,
} from '@reigh/editor-sdk';

// ---------------------------------------------------------------------------
// DSL types
// ---------------------------------------------------------------------------

/** A single clip entry in the compiler canary DSL. */
export interface CanaryDslClip {
  /** Stable ID for the generated clip (used as the timeline clip ID). */
  id: string;
  /** Target track ID (must exist in snapshot). */
  track: string;
  /** Start position in seconds. */
  at: number;
  /** Duration in seconds (hold clip). */
  duration: number;
  /** Clip type, defaults to 'hold'. */
  clipType?: string;
  /** 1-based source line number for source-map linking. */
  sourceLine?: number;
}

/** The compiler canary DSL document. */
export interface CanaryDsl {
  /** DSL format version. */
  version: 1;
  /** Virtual source URI for source-map entries. */
  sourceUri?: string;
  /** Clips to generate. */
  clips: CanaryDslClip[];
}

/** Result returned by the compiler canary after processing a DSL. */
export interface CompilerCanaryResult {
  /** The proposal created (or undefined if the DSL was empty). */
  proposal?: TimelineProposal;
  /** IDs of clips generated/updated by this run. */
  generatedClipIds: string[];
  /** Source-map entry IDs created for this run. */
  sourceMapEntryIds: string[];
}

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

/** Well-known key for GeneratedObjectMeta in clip app data. */
const GENERATED_META_KEY = '__generated__';

/** Project-data key prefix for DSL source storage. */
const DSL_KEY_PREFIX = '__dsl__:';

/** Project-data key prefix for source-map entries. */
const SM_KEY_PREFIX = '__sm__:';

/** Contribution ID used in generated metadata. */
const CANARY_CONTRIBUTION_ID = 'compiler-canary-generator';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Generate a unique ID for source-map entries. */
let _nextSmId = 0;
function generateSmId(): string {
  _nextSmId += 1;
  return `cc-sme-${_nextSmId}-${Date.now().toString(36)}`;
}

/** Current epoch milliseconds. */
function now(): number {
  return Date.now();
}

/**
 * Build a GeneratedObjectMeta record for a clip generated by this canary.
 */
function buildGeneratedMeta(
  extensionId: string,
  sourceMapEntryId?: string,
): GeneratedObjectMeta {
  const meta: GeneratedObjectMeta = {
    extensionId,
    contributionId: CANARY_CONTRIBUTION_ID,
    generatedAt: now(),
  };
  if (sourceMapEntryId) {
    meta.sourceMapEntryId = sourceMapEntryId;
  }
  return meta;
}

/**
 * Serialize a CanaryDsl to a JSON string.
 */
function serializeDsl(dsl: CanaryDsl): string {
  return JSON.stringify(dsl);
}

/**
 * Parse a CanaryDsl from a JSON string.
 */
function parseDsl(raw: string): CanaryDsl | null {
  try {
    const parsed = JSON.parse(raw);
    if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) return null;
    if (parsed.version !== 1) return null;
    if (!Array.isArray(parsed.clips)) return null;
    return parsed as CanaryDsl;
  } catch {
    return null;
  }
}

/**
 * Build project-data.write operations for the DSL source.
 */
function buildDslWriteOps(
  extensionId: string,
  dsl: CanaryDsl,
): TimelinePatchOperation[] {
  const sourceUri = dsl.sourceUri ?? `dsl://${extensionId}/main.json`;
  return [
    {
      op: 'project-data.write',
      target: extensionId,
      payload: {
        key: `${DSL_KEY_PREFIX}${sourceUri}`,
        value: serializeDsl(dsl),
        mode: 'replace',
      },
    },
  ];
}

/**
 * Build project-data.write operations for source-map entries.
 */
function buildSourceMapOps(
  extensionId: string,
  dsl: CanaryDsl,
  clipIdToSmId: Map<string, string>,
): TimelinePatchOperation[] {
  const ops: TimelinePatchOperation[] = [];
  const sourceUri = dsl.sourceUri ?? `dsl://${extensionId}/main.json`;

  for (let i = 0; i < dsl.clips.length; i++) {
    const clip = dsl.clips[i];
    const smId = generateSmId();
    clipIdToSmId.set(clip.id, smId);

    const sourceLine = clip.sourceLine ?? i + 3; // approximate line number
    const entry: Record<string, unknown> = {
      id: smId,
      source: extensionId,
      targetId: clip.id,
      targetGranularity: 'clip',
      sourceUri,
      sourceStartLine: sourceLine,
      sourceStartColumn: 0,
      sourceEndLine: sourceLine,
      sourceEndColumn: 0,
      stale: false,
    };

    ops.push({
      op: 'project-data.write',
      target: extensionId,
      payload: {
        key: `${SM_KEY_PREFIX}${smId}`,
        value: entry,
        mode: 'replace',
      },
    });
  }

  return ops;
}

/**
 * Build clip.add operations for each DSL clip entry.
 * Each clip carries generated-object metadata in its app payload.
 */
function buildClipAddOps(
  extensionId: string,
  snapshot: TimelineSnapshot,
  dsl: CanaryDsl,
  clipIdToSmId: Map<string, string>,
): TimelinePatchOperation[] {
  const ops: TimelinePatchOperation[] = [];

  // Build a set of clip IDs already owned by this extension (handled by update/remove)
  const snapshotClips = new Map<string, TimelineClipSummary>();
  for (const c of snapshot.clips) {
    snapshotClips.set(c.id, c);
  }

  for (let i = 0; i < dsl.clips.length; i++) {
    const clip = dsl.clips[i];

    // Skip clips already owned by this extension — they're handled by update/remove
    const existing = snapshotClips.get(clip.id);
    if (existing && isOwnedByExtension(existing, extensionId)) continue;
    const smId = clipIdToSmId.get(clip.id);
    const generatedMeta = buildGeneratedMeta(extensionId, smId);

    ops.push({
      op: 'clip.add',
      target: clip.id,
      order: i,
      payload: {
        track: clip.track,
        at: clip.at,
        clipType: clip.clipType ?? 'hold',
        hold: clip.duration,
        app: {
          [extensionId]: { source: 'compiler-canary' },
          [GENERATED_META_KEY]: generatedMeta,
        },
      },
    });
  }

  return ops;
}

/**
 * Build clip.remove operations for clips that are managed by this extension
 * but are no longer in the DSL.
 */
function buildClipRemoveOps(
  extensionId: string,
  snapshot: TimelineSnapshot,
  dslClipIds: Set<string>,
): TimelinePatchOperation[] {
  const ops: TimelinePatchOperation[] = [];

  for (const clipSummary of snapshot.clips) {
    if (dslClipIds.has(clipSummary.id)) continue;
    if (!isOwnedByExtension(clipSummary, extensionId)) continue;

    ops.push({
      op: 'clip.remove',
      target: clipSummary.id,
    });
  }

  return ops;
}

/**
 * Check whether a clip summary is owned by the given extension.
 * Uses the __generated__ metadata or extension namespace in app data.
 */
function isOwnedByExtension(
  clip: TimelineClipSummary,
  extensionId: string,
): boolean {
  // Check generatedMeta
  if (clip.generatedMeta && clip.generatedMeta.extensionId === extensionId) {
    return true;
  }
  // Check managedBy
  if (clip.managed && clip.managedBy === extensionId) {
    return true;
  }
  return false;
}

/**
 * Build clip.update operations for clips that exist in both the snapshot
 * and the DSL but with different properties.
 */
function buildClipUpdateOps(
  extensionId: string,
  snapshot: TimelineSnapshot,
  dsl: CanaryDsl,
  clipIdToSmId: Map<string, string>,
): TimelinePatchOperation[] {
  const ops: TimelinePatchOperation[] = [];
  const snapshotClips = new Map<string, TimelineClipSummary>();
  for (const c of snapshot.clips) {
    snapshotClips.set(c.id, c);
  }

  for (let i = 0; i < dsl.clips.length; i++) {
    const dslClip = dsl.clips[i];
    const existing = snapshotClips.get(dslClip.id);
    if (!existing) continue; // handled by add
    if (!isOwnedByExtension(existing, extensionId)) continue; // not ours

    const needsUpdate =
      existing.track !== dslClip.track ||
      Math.abs(existing.at - dslClip.at) > 0.001 ||
      Math.abs(existing.duration - dslClip.duration) > 0.001;

    if (needsUpdate) {
      const smId = clipIdToSmId.get(dslClip.id);
      const generatedMeta = buildGeneratedMeta(extensionId, smId);

      ops.push({
        op: 'clip.update',
        target: dslClip.id,
        order: i,
        payload: {
          track: dslClip.track,
          at: dslClip.at,
          hold: dslClip.duration,
          app: {
            [extensionId]: { source: 'compiler-canary' },
            [GENERATED_META_KEY]: generatedMeta,
          },
        },
      });
    }
  }

  return ops;
}

