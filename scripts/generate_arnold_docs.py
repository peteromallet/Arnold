def _literal_values(alias: Any) -> tuple[str, ...]:
    return tuple(str(value) for value in sorted(get_args(alias)))


def _source_path(obj: Any) -> str:
    return _relative(Path(inspect.getsourcefile(obj) or ""))


def _relative(path: Path) -> str:
    try:
        return path.resolve().relative_to(REPO_ROOT).as_posix()
    except ValueError:
        return path.as_posix()


def _annotation_name(annotation: Any) -> str:
    if annotation is type(None):
        return "None"
    origin = get_origin(annotation)
    if origin is Annotated:
        args = get_args(annotation)
        return _annotation_name(args[0]) if args else "Annotated"
    if origin in {Union, types.UnionType}:
        return " | ".join(_annotation_name(arg) for arg in get_args(annotation))
    text = getattr(annotation, "__name__", None)
    if text:
        return text
    return str(annotation).replace("typing.", "")


def _default_description(field: Any) -> str:
    if field.is_required():
        return "required"
    if field.default_factory is not None:
        factory = getattr(field.default_factory, "__name__", repr(field.default_factory))
        return f"default_factory={factory}"
    return repr(field.default)


def _pydantic_field_rows(model: type[Any]) -> list[tuple[str, str, str]]:
    rows = []
    for name, field in sorted(model.model_fields.items()):
        rows.append((name, _annotation_name(field.annotation), _default_description(field)))
    return rows


def _dataclass_field_rows(model: type[Any]) -> list[tuple[str, str, str]]:
    rows = []
    for field in sorted(dataclasses.fields(model), key=lambda item: item.name):
        if field.default is not dataclasses.MISSING:
            default = repr(field.default)
        elif field.default_factory is not dataclasses.MISSING:  # type: ignore[comparison-overlap]
            factory = getattr(field.default_factory, "__name__", repr(field.default_factory))
            default = f"default_factory={factory}"
        else:
            default = "required"
        rows.append((field.name, _annotation_name(field.type), default))
    return rows


def _table(headers: tuple[str, ...], rows: Iterable[tuple[Any, ...]]) -> list[str]:
    def cell(value: Any) -> str:
        return str(value).replace("\n", " ").replace("|", "\\|")

    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(cell(value) for value in row) + " |")
    return lines


def _node_name(node: ast.AST) -> str | None:
    if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
        return node.name
    if isinstance(node, ast.Assign):
        for target in node.targets:
            if isinstance(target, ast.Name):
                return target.id
    if isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
        return node.target.id
    return None


def _source_lines_for_symbols(path: Path, symbols: Iterable[str]) -> str:
    wanted = set(symbols)
    source = path.read_text(encoding="utf-8")
    lines = source.splitlines()
    tree = ast.parse(source, filename=str(path))
    ranges: list[tuple[int, int]] = []
    for node in tree.body:
        name = _node_name(node)
        if name not in wanted:
            continue
        end_lineno = getattr(node, "end_lineno", None)
        if end_lineno is None:
            continue
        ranges.append((node.lineno, end_lineno))
    ranges.sort()

    chunks: list[str] = []
    previous_end = 0
    for start, end in ranges:
        if chunks and start > previous_end + 1:
            chunks.append("")
        chunks.extend(lines[start - 1:end])
        previous_end = end
    return "\n".join(chunks).rstrip() + "\n"


def _pipeline_check_result(name: str) -> tuple[bool, str]:
    from argparse import Namespace

    from megaplan.cli import _handle_pipelines

    args = Namespace(pipelines_action="check", pipeline_name=name)
    stdout = io.StringIO()
    stderr = io.StringIO()
    with contextlib.redirect_stdout(stdout), contextlib.redirect_stderr(stderr):
        rc = _handle_pipelines(REPO_ROOT, args)
    details = "\n".join(
        part.strip()
        for part in (stdout.getvalue(), stderr.getvalue())
        if part.strip()
    )
    return rc == 0, details


def _example_pack_exists(pack: ExamplePack) -> bool:
    return (pack.source_dir / "__init__.py").is_file() and (
        pack.source_dir / "SKILL.md"
    ).is_file()


def _render_example(pack: ExamplePack) -> str | None:
    if not _example_pack_exists(pack):
        if pack.required:
            raise RuntimeError(f"required Arnold example pack is missing: {pack.slug}")
        return None

    check_ok, check_details = _pipeline_check_result(pack.slug)
    if not check_ok:
        if pack.required:
            raise RuntimeError(
                f"required Arnold example pack failed `megaplan pipelines check {pack.slug}`: "
                f"{check_details}"
            )
        return None

    init_path = pack.source_dir / "__init__.py"
    steps_path = pack.source_dir / "steps.py"
    skill_path = pack.source_dir / "SKILL.md"
    source_rows = [
        ("Package", _relative(pack.source_dir)),
        ("Manifest and builder", _relative(init_path)),
        ("Skill", _relative(skill_path)),
        ("Validation", f"`megaplan pipelines check {pack.slug}`"),
    ]
    if pack.step_symbols and steps_path.is_file():
        source_rows.insert(2, ("Steps", _relative(steps_path)))

    lines: list[str] = [
        GENERATED_HEADER.rstrip(),
        "",
        f"# {pack.title}",
        "",
        *pack.notes,
        "",
        "## Source Pack",
        "",
        *_table(("item", "value"), source_rows),
        "",
        "## Builder Surface",
        "",
        "The following snippet is extracted verbatim from the pack's `__init__.py`.",
        "",
        "```python",
        _source_lines_for_symbols(init_path, pack.init_symbols).rstrip(),
        "```",
        "",
    ]

    if pack.step_symbols and steps_path.is_file():
        lines.extend(
            [
                "## Step Surface",
                "",
                "The following snippet is extracted verbatim from the pack's `steps.py`.",
                "",
                "```python",
                _source_lines_for_symbols(steps_path, pack.step_symbols).rstrip(),
                "```",
                "",
            ]
        )

    lines.extend(
        [
            "## Package Skill",
            "",
            "The following module instructions are extracted verbatim from the pack's `SKILL.md`.",
            "",
            "````markdown",
            skill_path.read_text(encoding="utf-8").strip(),
            "````",
            "",
        ]
    )
    return "\n".join(lines).rstrip() + "\n"


def render_examples() -> dict[Path, str]:
    rendered: dict[Path, str] = {}
    for pack in sorted(EXAMPLE_PACKS, key=lambda item: item.slug):
        content = _render_example(pack)
        if content is not None:
            rendered[EXAMPLES_DIR / f"{pack.doc_slug}.md"] = content
    return rendered


def _literal_table(name: str, values: Iterable[str], source: str) -> list[tuple[str, str, str]]:
    return [(name, value, source) for value in sorted(values)]


def _manifest_rows() -> list[tuple[str, str]]:
    manifest_fields = {
        field.name: _annotation_name(field.type)
        for field in dataclasses.fields(discovery_manifest.Manifest)
    }
    return [
        (field_name, manifest_fields.get(field_name, "object"))
        for field_name in sorted(discovery_manifest.REQUIRED_FIELDS)
    ]


def _content_type_rows() -> list[tuple[str, str]]:
    return [(name, CONTENT_TYPES.get(name)) for name in CONTENT_TYPES.names()]


def _legal_coercion_rows() -> list[tuple[str, str, str]]:
    explicit = [
        (from_ct, to_ct, "registered")
        for from_ct, to_ct in sorted(contracts.legal_coercions)
    ]
    identity = [(name, name, "identity") for name in CONTENT_TYPES.names()]
    return sorted({*explicit, *identity})


def _repair_gradient_error_kinds() -> tuple[str, ...]:
    doc = inspect.getdoc(contracts.RepairGradient) or ""
    match = re.search(r"one of ([^.]+)\.", doc)
    if not match:
        return ()
    return tuple(sorted(re.findall(r"``([^`]+)``", match.group(1))))


def _string_template(node: ast.AST) -> str:
    if isinstance(node, ast.Constant):
        return repr(node.value) if not isinstance(node.value, str) else node.value
    if isinstance(node, ast.JoinedStr):
        parts: list[str] = []
        for value in node.values:
            if isinstance(value, ast.Constant):
                parts.append(str(value.value))
            elif isinstance(value, ast.FormattedValue):
                parts.append("{...}")
        return "".join(parts)
    if isinstance(node, ast.BinOp) and isinstance(node.op, ast.Add):
        return f"{_string_template(node.left)}{_string_template(node.right)}"
    return ast.unparse(node)


def _defect_append_rows(module: Any, list_name: str) -> list[tuple[str, str]]:
    source = inspect.getsource(module)
    tree = ast.parse(source)
    rows: list[tuple[str, str]] = []
    for node in ast.walk(tree):
        if not isinstance(node, ast.Call):
            continue
        func = node.func
        if not (isinstance(func, ast.Attribute) and func.attr == "append" and node.args):
            continue
        receiver = func.value
        local_list = isinstance(receiver, ast.Name) and receiver.id == list_name
        attribute_list = isinstance(receiver, ast.Attribute) and receiver.attr == list_name
        if not (local_list or attribute_list):
            continue
        rows.append((f"{module.__name__}:{node.lineno}", _string_template(node.args[0])))
    return sorted(rows)


def _cli_subcommand_rows(parser: argparse.ArgumentParser) -> list[tuple[str, str, str]]:
    rows: list[tuple[str, str, str]] = []
    for action in parser._actions:
        choices = getattr(action, "choices", None)
        if not isinstance(choices, dict):
            continue
        for name, subparser in sorted(choices.items()):
            if not isinstance(subparser, argparse.ArgumentParser):
                continue
            for sub_action in subparser._actions:
                sub_choices = getattr(sub_action, "choices", None)
                if not isinstance(sub_choices, dict):
                    continue
                for sub_name in sorted(sub_choices):
                    rows.append((name, sub_name, _source_path(build_parser)))
    return rows


def _doctor_option_rows(parser: argparse.ArgumentParser) -> list[tuple[str, str]]:
    for action in parser._actions:
        choices = getattr(action, "choices", None)
        if isinstance(choices, dict) and "doctor" in choices:
            doctor_parser = choices["doctor"]
            rows: list[tuple[str, str]] = []
            for doctor_action in doctor_parser._actions:
                for option in doctor_action.option_strings:
                    rows.append((option, doctor_action.help or ""))
            return sorted(rows)
    return []


def _state_rows() -> list[tuple[str, str, str]]:
    rows: list[tuple[str, str, str]] = []
    for name in sorted(dir(megaplan_types)):
        if name.startswith("STATE_"):
            value = getattr(megaplan_types, name)
            if isinstance(value, str):
                rows.append((name, value, _source_path(megaplan_types)))
    return rows


