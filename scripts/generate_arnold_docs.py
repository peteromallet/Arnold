def generated_files(docs_root: Path) -> dict[Path, str]:
    files = {docs_root / REFERENCE_PATH: render_reference()}
    for relative_path, content in render_examples().items():
        files[docs_root / relative_path] = content
    return files


def _check_files(files: dict[Path, str]) -> list[str]:
    failures: list[str] = []
    for path, expected in files.items():
        try:
            current = path.read_text(encoding="utf-8")
        except FileNotFoundError:
            failures.append(f"{_relative(path)} is missing")
            continue
        if current != expected:
            diff = difflib.unified_diff(
                current.splitlines(),
                expected.splitlines(),
                fromfile=f"{_relative(path)} (current)",
                tofile=f"{_relative(path)} (generated)",
                lineterm="",
            )
            failures.append("\n".join(diff))
    return failures


def write_files(files: dict[Path, str]) -> None:
    for path, content in sorted(files.items(), key=lambda item: item[0].as_posix()):
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--check", action="store_true", help="Fail if generated docs are stale.")
    mode.add_argument("--write", action="store_true", help="Write generated docs to disk.")
    parser.add_argument(
        "--docs-root",
        type=Path,
        default=DEFAULT_DOCS_ROOT,
        help=argparse.SUPPRESS,
    )
    args = parser.parse_args(argv)

    files = generated_files(args.docs_root)
    if args.write:
        write_files(files)
        for path in sorted(files):
            print(f"wrote {_relative(path)}")
        return 0

    failures = _check_files(files)
    if failures:
        print("Arnold generated docs are stale. Run `python scripts/generate_arnold_docs.py --write`.")
        print()
        print("\n\n".join(failures))
        return 1
    print("Arnold generated docs are up to date.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
